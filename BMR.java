package com.healthfoodenergy;

public class BMR {
	
	double height;
	double wieght;
	int age;
	String gender;
	String work;
	public String getWork() {
		return work;
	}
	public void setWork(String work) {
		this.work = work;
	}
	public double getHeight() {
		return height;
	}
	public void setHeight(double height) {
		this.height = height;
	}
	public double getWieght() {
		return wieght;
	}
	public void setWieght(double wieght) {
		this.wieght = wieght;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	

}
